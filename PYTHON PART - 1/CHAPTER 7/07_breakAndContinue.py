for i in range(10) :

    if(i==5) :
        break

    print("The number is " + str(i))

else :
    print("The else in the for loop is invoked!")


print("\nDone")


for i in range(10) :

    if(i==5) :
        continue

    print("The number is " + str(i))

else :
    print("The else in the for loop is invoked!")


print("\nDone")

